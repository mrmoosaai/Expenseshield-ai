    pdf.ln(20)
    
    # ===== SECTION 6: Additional Notes =====
    pdf.set_font('Arial', 'B', 12)
    pdf.set_text_color(41, 128, 185)
    pdf.cell(0, 10, 'NEXT STEPS & CONTACT', 0, 1)
    
