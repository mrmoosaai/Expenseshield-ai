def generate_professional_expense_pdf(expense_data: dict, result: dict) -> str:
    """
    Generate professional PDF with charts and branding
    """
    # If fpdf is not available, write a simple text summary instead
    if not HAS_FPDF:
        safe_desc = expense_data.get('description', 'expense').replace(' ', '_').replace('/', '_')[:30]
        filename = f"expense_report_{safe_desc}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.txt"
        filepath = os.path.join(os.getcwd(), filename)
        with open(filepath, 'w', encoding='utf-8') as fh:
            fh.write('EXPENSE APPROVAL REPORT\n')
            fh.write('Generated: ' + datetime.now().strftime('%Y-%m-%d %H:%M:%S') + '\n\n')
            fh.write(f"Amount: {expense_data.get('amount', 0)}\n")
            fh.write(f"Description: {expense_data.get('description', 'N/A')}\n")
            fh.write(f"Status: {result.get('status', 'N/A')}\n")
            fh.write(f"Reason: {result.get('reason', 'N/A')}\n")
        return filepath

    # Generate unique expense reference ID
    import random
    ref_id = f"EXP-{datetime.now().strftime('%Y%m%d%H%M%S')}-{random.randint(1000, 9999)}"

    pdf = ProfessionalExpensePDF()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=15)
    
    # ===== SECTION 1: Executive Summary =====
    pdf.set_font('Arial', 'B', 14)
    pdf.set_text_color(41, 128, 185)
    pdf.cell(0, 10, 'EXECUTIVE SUMMARY', 0, 1)
    
    # Summary box
    pdf.set_fill_color(236, 240, 241)
    pdf.rect(10, pdf.get_y(), 190, 30, 'F')
    
    pdf.set_xy(15, pdf.get_y() + 5)
    pdf.set_font('Arial', '', 11)
    pdf.set_text_color(0, 0, 0)
    pdf.cell(90, 8, f'Reference ID: {ref_id}', 0, 0)
    pdf.cell(90, 8, f'Date: {datetime.now().strftime("%Y-%m-%d")}', 0, 1)
    pdf.set_x(15)
    pdf.cell(90, 8, f'Processing Time: < 2 seconds', 0, 0)
    pdf.cell(90, 8, f'System: ExpenseShield AI v1.0', 0, 1)
    
    pdf.ln(20)
    
    # ===== SECTION 2: Expense Details (Professional Table) =====
    pdf.set_font('Arial', 'B', 14)
    pdf.set_text_color(41, 128, 185)
    pdf.cell(0, 10, 'EXPENSE DETAILS', 0, 1)
    
    # Table header
    pdf.set_fill_color(41, 128, 185)
    pdf.set_text_color(255, 255, 255)
    pdf.set_font('Arial', 'B', 11)
    
    pdf.cell(95, 10, 'Field', 1, 0, 'C', True)
    pdf.cell(95, 10, 'Information', 1, 1, 'C', True)
    
    # Table data
    pdf.set_text_color(0, 0, 0)
    pdf.set_font('Arial', '', 11)
    
    amount = expense_data.get('amount', 0)
    description = expense_data.get('description', 'N/A')
    status = result.get('status', 'N/A')
    reason = result.get('reason', 'N/A')
    if isinstance(reason, dict):
        reason = 'Reviewed through standard compliance checks.'
    elif isinstance(reason, str):
        reason = reason[:60] if len(reason) > 60 else reason
    else:
        reason = 'Standard verification passed.'
    llm_used = result.get('llm_used', False)
    human_review = result.get('human_review', False)
    
    # Color code based on status
    if status == 'APPROVED':
        status_color = (39, 174, 96)  # Green
    elif status == 'REJECTED':
        status_color = (192, 57, 43)  # Red
    else:
        status_color = (0, 0, 0)  # Black
    
    rows = [
        ('Expense Amount', f'$ {amount:,.2f} USD'),
        ('Description', description[:50] + '...' if len(description) > 50 else description),
        ('Approval Status', status),
        ('Decision Reason', reason),
        ('Reference ID', ref_id),
        ('Submission Time', datetime.now().strftime('%Y-%m-%d %H:%M:%S')),
    ]
    
    for i, (field, detail) in enumerate(rows):
        # Alternate row colors
        if i % 2 == 0:
            pdf.set_fill_color(245, 247, 250)
        else:
            pdf.set_fill_color(255, 255, 255)
        
        pdf.cell(95, 10, field, 1, 0, 'L', True)
        
        # Special formatting for status
